import pandas as pd
data=pd.read_csv('/content/air quality data.csv')
data.head()

x=data.loc[:,['PM2.5',	'PM10',	'NO2',	'SO2',	'CO',	'O3',	'Dominant_Pollutant']]
y=data.AQI_Category
x.head()

from sklearn.preprocessing import LabelEncoder
enc=LabelEncoder()
x.Dominant_Pollutant=enc.fit_transform(x.Dominant_Pollutant)
x.head()

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y)

from sklearn.ensemble import RandomForestClassifier
model=RandomForestClassifier()
model.fit(x_train,y_train)

from sklearn.metrics import accuracy_score
y_predict=model.predict(x_test)
accuracy_score(y_test,y_predict)

pd.crosstab(y_test,y_predict)

model.predict([['23.75',	63.34,	17.21,	2.82,	1.08,	25.82,	2]])

